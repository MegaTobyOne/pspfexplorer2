/**
 * One-shot generator: reads v2 domain modules from archive/v2/scripts/domains/
 * and emits typed v3 ES modules under src/pspf/.
 *
 * Run from repo root:  node scripts/port-pspf-data.mjs
 */

import { writeFile, mkdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');
const v2Dir = resolve(repoRoot, 'archive/v2/scripts/domains');
const outDir = resolve(repoRoot, 'src/pspf');

const DOMAINS = [
  {
    key: 'governance',
    module: 'governance.js',
    domainExport: 'governanceDomain',
    reqsExport: 'governanceRequirements',
  },
  {
    key: 'information',
    module: 'information.js',
    domainExport: 'informationDomain',
    reqsExport: 'informationRequirements',
  },
  {
    key: 'personnel',
    module: 'personnel.js',
    domainExport: 'personnelDomain',
    reqsExport: 'personnelRequirements',
  },
  {
    key: 'physical',
    module: 'physical.js',
    domainExport: 'physicalDomain',
    reqsExport: 'physicalRequirements',
  },
  { key: 'risk', module: 'risk.js', domainExport: 'riskDomain', reqsExport: 'riskRequirements' },
  {
    key: 'technology',
    module: 'technology.js',
    domainExport: 'technologyDomain',
    reqsExport: 'technologyRequirements',
  },
];

const PREFIXES = {
  governance: ['GOV'],
  information: ['INFO'],
  personnel: ['PERS'],
  physical: ['PHYS'],
  risk: ['RISK'],
  technology: ['TECH'],
};

const E8_KEY_BY_HINT = [
  [/patch\s*applications?/i, 'patch-applications'],
  [/patch.*operating/i, 'patch-operating-systems'],
  [/multi[-\s]?factor/i, 'multi-factor-authentication'],
  [/admin(istrative)?\s*priv/i, 'restrict-administrative-privileges'],
  [/application\s*control/i, 'application-control'],
  [/macros/i, 'restrict-microsoft-office-macros'],
  [/user[\s-]?application\s*hardening/i, 'user-application-hardening'],
  [/backups?/i, 'regular-backups'],
];

function inferE8(text) {
  for (const [re, key] of E8_KEY_BY_HINT) if (re.test(text)) return key;
  return undefined;
}

function ts(value) {
  return JSON.stringify(value);
}

function escapeBackticks(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

async function main() {
  await mkdir(outDir, { recursive: true });

  const allIds = new Set();
  const summary = [];

  for (const d of DOMAINS) {
    const v2Path = resolve(v2Dir, d.module);
    const mod = await import(v2Path);
    const v2Domain = mod[d.domainExport];
    const v2Reqs = mod[d.reqsExport];

    if (!v2Reqs || typeof v2Reqs !== 'object') {
      throw new Error(`Missing ${d.reqsExport} in ${v2Path}`);
    }

    const reqIds = Object.keys(v2Reqs).sort();
    const lines = [];
    lines.push(`// AUTO-GENERATED by scripts/port-pspf-data.mjs — do not edit by hand.`);
    lines.push(`// Re-run after updating PSPF source data.`);
    lines.push(``);
    lines.push(`import type { Domain, Requirement } from '../data/types.ts';`);
    lines.push(`import { asRequirementId } from '../data/types.ts';`);
    lines.push(``);
    lines.push(`export const ${d.key}Domain: Domain = {`);
    lines.push(`  key: ${ts(d.key)},`);
    lines.push(`  name: ${ts(v2Domain?.title ?? d.key)},`);
    lines.push(`  description: ${ts(v2Domain?.description ?? '')},`);
    lines.push(`  prefixes: ${ts(PREFIXES[d.key])},`);
    lines.push(`};`);
    lines.push(``);
    lines.push(`export const ${d.key}Requirements: readonly Requirement[] = [`);
    for (const id of reqIds) {
      if (allIds.has(id)) {
        throw new Error(`Duplicate requirement id across domains: ${id}`);
      }
      allIds.add(id);
      const r = v2Reqs[id];
      const rawDesc = String(r.description ?? '');
      // Pull out trailing parenthetical "(NN. Section name)" and use it as section context only via title fallback.
      const text = rawDesc.replace(/\s*\(\d+\.[^)]*\)\s*$/, '').trim();
      const title = String(r.title ?? text).trim();
      const e8 = d.key === 'technology' ? inferE8(`${title} ${text}`) : undefined;

      lines.push(`  {`);
      lines.push(`    id: asRequirementId(${ts(id)}),`);
      lines.push(`    domain: ${ts(d.key)},`);
      lines.push(`    title: \`${escapeBackticks(title)}\`,`);
      lines.push(`    text: \`${escapeBackticks(text)}\`,`);
      lines.push(`    reportingType: 'unspecified',`);
      if (e8) lines.push(`    essentialEightControl: ${ts(e8)},`);
      lines.push(`  },`);
    }
    lines.push(`];`);
    lines.push(``);

    await writeFile(resolve(outDir, `${d.key}.ts`), lines.join('\n'));
    summary.push({ key: d.key, count: reqIds.length });
  }

  // Index module
  const indexLines = [];
  indexLines.push(`// AUTO-GENERATED by scripts/port-pspf-data.mjs — do not edit by hand.`);
  indexLines.push(``);
  indexLines.push(`import type { Domain, Requirement, RequirementId } from '../data/types.ts';`);
  for (const d of DOMAINS) {
    indexLines.push(`import { ${d.key}Domain, ${d.key}Requirements } from './${d.key}.ts';`);
  }
  indexLines.push(`import { essentialEightControls } from './essential-eight.ts';`);
  indexLines.push(``);
  indexLines.push(`export const allDomains: readonly Domain[] = [`);
  for (const d of DOMAINS) indexLines.push(`  ${d.key}Domain,`);
  indexLines.push(`];`);
  indexLines.push(``);
  indexLines.push(`export const allRequirements: readonly Requirement[] = [`);
  for (const d of DOMAINS) indexLines.push(`  ...${d.key}Requirements,`);
  indexLines.push(`];`);
  indexLines.push(``);
  indexLines.push(
    `export const requirementById: ReadonlyMap<RequirementId, Requirement> = new Map(`,
  );
  indexLines.push(`  allRequirements.map((r) => [r.id, r]),`);
  indexLines.push(`);`);
  indexLines.push(``);
  indexLines.push(
    `export const requirementsByDomain: ReadonlyMap<string, readonly Requirement[]> = new Map(`,
  );
  indexLines.push(
    `  allDomains.map((d) => [d.key, allRequirements.filter((r) => r.domain === d.key)]),`,
  );
  indexLines.push(`);`);
  indexLines.push(``);
  indexLines.push(
    `export type { Domain, Requirement, EssentialEightControl } from '../data/types.ts';`,
  );
  indexLines.push(`export { essentialEightControls };`);
  indexLines.push(``);
  await writeFile(resolve(outDir, 'index.ts'), indexLines.join('\n'));

  // Summary
  const total = summary.reduce((acc, s) => acc + s.count, 0);
  console.log('Per-domain counts:');
  for (const s of summary) console.log(`  ${s.key.padEnd(12)} ${s.count}`);
  console.log(`Total requirements: ${total}`);
}

await main();
